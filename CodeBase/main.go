package main

import (
	"os"

	"github.com/codebase/cmd"
)

// main делегирует весь lifecycle (жизненный цикл) CLI-пакету cmd.
// Здесь намеренно нет собственной логики, чтобы точка входа оставалась
// минимальной и не дублировала обработку команд/флагов.
func main() {
	// Cobra сам печатает пользовательские ошибки на верхнем уровне команд,
	// а main отвечает только за корректный код завершения процесса.
	if err := cmd.Execute(); err != nil {
		os.Exit(1)
	}
}
